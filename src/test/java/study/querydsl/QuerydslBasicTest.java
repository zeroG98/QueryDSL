    @Test
    public void search() {
        Member findMember = queryFactory
                .selectFrom(member)
                .where(member.username.eq("member1")
                        .and(member.age.eq(10)))
                .fetchOne();

        assertThat(findMember.getUsername()).isEqualTo("member1");
        /*검색조건 종류
         * .eq -> username = 'member1'
         * .ne, eq.not() -> username != 'member1'
         * .isNotNull() 이름이 is not null
         * .in() -> ~이거나
         * .notIn() -> ~이거나 ~이 아닌
         * .between() ->  ~와 ~사이
         * .goe() -> >=
         * .gt() -> >
         * .loe -> <=
         * .lt -> <
         * .like() -> like 검색
         * .contains("member") -> like '%member%' 검색
         * .startWith("member") -> like 'member%' 검색*/
    }

